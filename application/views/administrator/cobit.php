<p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;COBIT dikembangkan oleh IT Governance Institute (ITGI), yang merupakan bagian dari ISACA. COBIT (Control Objectives for Information and Related Technology) adalah sebuah framework dan alat pendukung yang membantu manajer menjembatani gap antara tujuan untuk keperluan pengendalian, permasalahan teknik (technical issue) dan risiko bisnis serta mengkomunikasikan level pengendalian kepada stakeholders.</p>
<p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;COBIT membantu menyokong pengembangan kebijakan yang jelas dan langkah-langkah praktis terbaik yang dapat diambil untuk pengendalian teknologi informasi di seluruh perusahaan. Pengelolaan assurance, pengendalian dan security professionals. COBIT 4.1 menyediakan langkah-langkah praktis terbaik yang dapat diambil dan lebih difokuskan pada pengendalian (control), yang selanjutnya dijelaskan dalam domain dan framework proses</p>
    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;COBIT mendefinisikan aktivitas TI kedalam sebuah model proses dengan empat domain yaitu :<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   1.	Plan and Organise (perencanaan dan organisasi)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   2.	Acquire and Implement (akuisisi dan implementasi)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   3.	Deliver and Support (pengiriman dan dukungan)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  4.	Monitor and Evaluate (pemantauan dan evaluasi). <br>
    <center><img src="<?= base_url()?>assets/templates/images/cobit.png"></center><br>
        &nbsp;&nbsp;&nbsp;&nbsp;  Kerangka COBIT menyediakan model proses referensi dan bahasa umum untuk semua orang dalam suatu perusahaan untuk melihat dan mengelola kegiatan TI. Penggabungan model operasional dan bahasa umum untuk semua bagian dari bisnis yang terlibat dalam TI adalah salah satu langkah awal dan yang paling penting menuju tata kelola yang baik.
    </p>
